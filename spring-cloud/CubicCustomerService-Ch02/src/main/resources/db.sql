DROP TABLE IF EXISTS CUSTOMER;
CREATE TABLE CUSTOMER (
		CUST_ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
		CUST_NO CHAR(12) NOT NULL,
		NAME VARCHAR(32) NOT NULL,
		EMAIL VARCHAR(32) NOT NULL,
		ADDRESS VARCHAR(128) );

